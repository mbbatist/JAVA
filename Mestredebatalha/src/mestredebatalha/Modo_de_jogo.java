    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jogo==4)
        {
            JOptionPane.showMessageDialog(null,"Guerra entre 2 jogadores confirmada!","Modo de Jogo",JOptionPane.INFORMATION_MESSAGE);
            new Selecao_de_tropas1().setVisible(true);
            this.dispose();
        }
        else
        {
            if(jogo==5)
        {
            JOptionPane.showMessageDialog(null,"Guerra entre jogador e computador confirmada!","Modo de Jogo",JOptionPane.INFORMATION_MESSAGE);
            new Selecao_de_tropas1().setVisible(true);
            this.dispose();
        }
            else
        {
            if(jogo==7)
        {
            JOptionPane.showMessageDialog(null,"Luta entre 2 jogadores confirmada!","Modo de Jogo",JOptionPane.INFORMATION_MESSAGE);
            new Selecao_de_campeoes().setVisible(true);
            this.dispose();
        }
            else
        {
            if(jogo==8)
        {
            JOptionPane.showMessageDialog(null,"Luta entre jogador e computador confirmada!","Modo de Jogo",JOptionPane.INFORMATION_MESSAGE);
            new Selecao_de_campeoes().setVisible(true);
            this.dispose();
        }
        }
        }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

